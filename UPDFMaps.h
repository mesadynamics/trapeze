inline char NameToCode(ConstMapParam inMap, const char* inName) {
	for(long j = 0; j < 256; j++) {
		if(strcmp(inName, inMap[j].name) == 0)
			return inMap[j].code;
	}
	
				
	return 0;
}

inline char AdobeNameToCode(const char* inName) {
	if(strlen(inName) > 1 && inName[0] == 'a' && isdigit(inName[1])) {
		unsigned long cl = strtol(&(inName[1]), (char**) NULL, 10);
		if(cl < 227) {
			char c = (char) cl + 29;
			
			if(isprint(c) || isspace(c))
				return c;

			switch(cl) {
				// 105? 391? 138? 121?
				
				case 134:
					return '\244'; // section
					
				case 120:
					return '\245'; // bullet	
																		
				case 177:	
					return '\320'; // emdash
					
				case 179:	
					return '\322'; // quotedblleft
					
				case 180:
					return '\323'; // quotedblright
					
				case 182:
					return '\325'; // quoteright
			}	
		}

		return '\245';
	}
	
	return 0;
}

